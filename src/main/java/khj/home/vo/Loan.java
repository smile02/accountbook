package khj.home.vo;

public class Loan {

	private int idx;
	private String nickname;
	private String loan_place;
	private int loan_price;
	private String loan_purpose;
	private String loan_date;
	private String orderdate;
	
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getLoan_place() {
		return loan_place;
	}
	public void setLoan_place(String loan_place) {
		this.loan_place = loan_place;
	}
	public int getLoan_price() {
		return loan_price;
	}
	public void setLoan_price(int loan_price) {
		this.loan_price = loan_price;
	}
	public String getLoan_purpose() {
		return loan_purpose;
	}
	public void setLoan_purpose(String loan_purpose) {
		this.loan_purpose = loan_purpose;
	}
	public String getLoan_date() {
		return loan_date;
	}
	public void setLoan_date(String loan_date) {
		this.loan_date = loan_date;
	}
	public String getOrderdate() {
		return orderdate;
	}
	public void setOrderdate(String orderdate) {
		this.orderdate = orderdate;
	}
	
	
}
